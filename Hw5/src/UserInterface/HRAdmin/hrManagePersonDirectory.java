    public void populateTable(){
        
        DefaultTableModel dtm = (DefaultTableModel) tblPersons.getModel();
        dtm.setRowCount(0);
        for (Person p: b.getPd().getPersonList()){
            Object[] row = new Object[4];
            row[0] = p;
            row[1] = p.getLastName();
            row[2] = "";
            for(UserAccount ua: p.getPersonAccountList()){
                row[2] = row[2] + "| " + ua.getUserName();
            }
           dtm.addRow(row);
        }
    
    }

