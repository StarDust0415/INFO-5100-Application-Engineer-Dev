    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = tblPersons.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select any Row");
        }else{
            Person p = (Person)tblPersons.getValueAt(selectedRow,0);
            
            hrUpdatePersonPanel sap = new hrUpdatePersonPanel(upc,p,b);
            upc.add("saUpdateAccountPanel",sap);
            CardLayout layout = (CardLayout) upc.getLayout();
            layout.next(upc);

        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String input = txtSearch.getText();
        resultList.clear();
        for(Person p: b.getPd().getPersonList()){
            if(p.getFirstName().equals(input)){
                resultList.add(p);
            }
        }
        
        populateResult();
        
        
        
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPersons.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select any Row");
        }else{
            Person p = (Person)tblPersons.getValueAt(selectedRow,0);
            
            b.getPd().getPersonList().remove(p);
            
            
            b.getUad().deleteAccountOfPerson(p.getFirstName());
            
            populateTable();
            
            
            
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    public void populateResult(){
        DefaultTableModel dtm = (DefaultTableModel) tblPersons.getModel();
        dtm.setRowCount(0);
        for (Person p: resultList){
            Object[] row = new Object[4];
            row[0] = p;
            row[1] = p.getLastName();
            row[2] = "";
            for(UserAccount ua: p.getPersonAccountList()){
                row[2] = row[2] + "| " + ua.getUserName();
            }
           dtm.addRow(row);
        }
    }

