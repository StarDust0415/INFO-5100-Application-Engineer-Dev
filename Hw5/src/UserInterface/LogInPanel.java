    public boolean isValidUser(String userName, String password){
        
        Boolean valid = false;
        
        for(UserAccount ua: b.getUad().getUserAccountList()){
            if( ua.getUserName().equals(userName) && ua.getPassword().equals(UserAccount.encryptPassword(password)) ){
                
                if(ua.getActive() == true){
                    if(ua.getRoleType().equals("System admin")){
                   
                    saWelcomePanel panel = new saWelcomePanel(upc,b,ua);
                    upc.add("saWelcomePanel",panel);
  System.out.println("1");
                
                    }else if(ua.getRoleType().equals("HR admin")){
                    
                    hrWelcomePanel panel = new hrWelcomePanel(upc,b,ua);
                    upc.add("hrWelcomePanle",panel);
  System.out.println("2");
                
                    }else if(ua.getRoleType().equals("common user")){
                    
                    commonWelcomePanel panel = new commonWelcomePanel(upc,b,ua);
                    upc.add("commonWelcomePanel",panel);
                            
                    }
                        valid  = true;
                        System.out.println("turn to true");

                }else{
                    JOptionPane.showMessageDialog(null,"Not an Active User");
                }
                
                
                
                
            }//place to put else
     
        }

        return valid;
    }

