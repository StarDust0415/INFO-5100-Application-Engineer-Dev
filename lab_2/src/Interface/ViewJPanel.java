    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        person.setName(nameTextField.getText());
        person.setAge(ageTextField.getText());
        person.setNationality(nationalityTextField.getText());
        person.setGender(genderTextField.getText());

        Address address = new Address();
        person.setAddress(address);
        address.setLine1(line1TextField.getText());
        address.setState(stateTextField.getText());
        address.setCity(cityTextField.getText());
        address.setLine2(line2TextField.getText());
        address.setZipcode(zipcodeTextField.getText());

        Checking checking = new Checking();
        financialaccounts.setChecking(checking);
        person.setFinancialaccounts(financialaccounts);
        checking.setCreateDate(createDateTextField.getText());
        checking.setActiveStatus(activeornotactiveTextField.getText());
        checking.setDebt(debtTextField.getText());
        checking.setCredit(creditTextField.getText());
        checking.setAccNum(checkingNumTextField.getText());

        Saving saving =new Saving();
        financialaccounts.setSaving(saving);
        person.setFinancialaccounts(financialaccounts);
        saving.setCreateDate(createDateTextField.getText());
        saving.setActiveStatus(activeornotTextField.getText());
        saving.setDebt(DebtTextField.getText());
        saving.setCredit(CreditTextField.getText());
        saving.setAccNum(savingTextField.getText());

        Licensedata licensedata = new Licensedata();
        person.setLicensedata(licensedata);
        licensedata.setIDNumber(IDNumTextField.getText());
        licensedata.setExpireDate(expiredateTextField.getText());
        licensedata.setIssueDate(issuedateTextField.getText());
        licensedata.setIssueState(issuestateTextField.getText());
        licensedata.setRestriction(restrictionTextField.getText());

        Creditdata creditdata = new Creditdata();
        person.setCreditdata(creditdata);
        creditdata.setCardNum(cardNumTextField.getText());
        creditdata.setCardType(cardtypeTextField.getText());
        creditdata.setExpireDate(expiredateTextField.getText());
        creditdata.setSecurityCode(securitycodeTextField.getText());
        creditdata.setName(nameTextField.getText());

        JOptionPane.showMessageDialog(null,"Create Product Successfully");

    }//GEN-LAST:event_jButton1ActionPerformed

    private void browseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseBtnActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        int result = chooser.showOpenDialog(null);
        if(result == JFileChooser.APPROVE_OPTION){
            String name = chooser.getSelectedFile().getPath();
            ImageIcon image = new ImageIcon(name);
            image.setImage(image.getImage().getScaledInstance(picLabel.getWidth(), picLabel.getHeight(),Image.SCALE_DEFAULT ));
            picLabel.setIcon(image);
            person.setImage(image);
        }

    }//GEN-LAST:event_browseBtnActionPerformed


